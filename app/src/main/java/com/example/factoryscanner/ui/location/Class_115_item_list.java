package com.example.factoryscanner.ui.location;

public class Class_115_item_list {

    public String Location, Notes, Location_Type, Warehouse;

    public Class_115_item_list(String Location, String Notes, String Location_Type, String Warehouse) {
        this.Location = Location;
        this.Notes = Notes;
        this.Location_Type = Location_Type;
        this.Warehouse = Warehouse;
    }
    public String getLocation() {
        return Location;
    }
    public String getNotes() {
        return Notes;
    }
    public String getLocation_Type() {
        return Location_Type;
    }
    public String getWarehouse() {
        return Warehouse;
    }
}
